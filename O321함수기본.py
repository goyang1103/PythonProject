def name_card(name, addr, phone):  # 매개 변수가 있는 함수, 반환 값은 없음
    print(f"주소 : {addr}")
    print(f"전화번호 : {phone}")
    print(f"이름 : {name}")
    print('-'*30)


name_card("안유진", "서울시 강남구 역삼동", "010-1234-5678")
